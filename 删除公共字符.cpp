#include <iostream>
#include <string>
using namespace std;

/*
这是一个删除公共字符的demo；
解法：首先找一个数组，记录str2中出现的字符；
然后找一个string，遍历str1，将与str2不重复的部分都放到这个string中，然后返回这个string
*/

int main()
{
	string str1, str2;
	getline(cin, str1);
	getline(cin, str2);
	int hashtable[256] = { 0 };
	for (size_t i = 0; i < str2.size(); ++i)
	{
		hashtable[str2[i]]++;
	}
	string ret;
	for (size_t i = 0; i < str1.size(); ++i)
	{
		if (hashtable[str1[i]] == 0)
		{
			ret += str1[i];
		}
	}
	cout << ret << endl;
	return 0;
}